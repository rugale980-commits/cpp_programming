#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Small
//  Description   : Write a recursive program which accept string from user and
//                  count number of small character.
//  Input         : String
//  Output        : Count small character
//  Author        : Rahul Balasaheb Ugale
//  Date          : 09/02/2026
//
////////////////////////////////////////////////////////////////////////////////

int Small(char *str)
{
    static int iCount = 0;

    if (*str != '\0')
    {
        if (*str >= 'a' && *str <= 'z')
        {
            iCount++;
        }

        str++;
        Small(str);
    }

    return iCount;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    string Ch;
    int iRet = 0;

    cout << "Enter the String : " << "\n";

    getline(cin, Ch);

    iRet = Small(&Ch[0]);

    cout << iRet;

    return 0;
}

