int Max(int iNo)
{
    static int iMin = 9;
    int iDigit = 0;

    if (iNo != 0)
    {
        iDigit = iNo % 10;

        if (iDigit < iMin)
        {
            iMin = iDigit;
        }

        Max(iNo / 10);
    }

    return iMin;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout << "Enter the Number : " << "\n";
    cin >> iValue;

    iRet = Max(iValue);

    cout << "Smallest number is : " << iRet;

    return 0;
}

