void Display(char ch)
{
    if (ch < 'a')
    {
        return;
    }

    Display(ch - 1);

    cout << ch << "\t";

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    char ch;

    cout << "Enter the Number : \n";
    cin >> iValue;

    ch = 'a' + iValue;

    Display(ch);

    return 0;

} // End of main

