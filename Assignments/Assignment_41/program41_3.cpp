int Strlen(char *str)
{
    if (*str == '\0')
    {
        return 0;
    }

    return (1 + Strlen(str + 1));

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    char ch[20];
    int iRet = 0;

    cout << "Enter the Character : \n";
    cin >> ch;

    iRet = Strlen(ch);

    cout << iRet;

    return 0;

} // End of main

