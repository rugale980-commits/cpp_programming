#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Mult
//  Description   : Write a recursive program which Accept number from user and
//                  its product of digits.
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 4/11/2025
//
////////////////////////////////////////////////////////////////////////////////

int Mult(int iNo)
{
    if (iNo == 0)
    {
        return 1;
    }

    return (iNo % 10) * Mult(iNo / 10);

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout << "Enter the Character : \n";
    cin >> iValue;

    iRet = Mult(iValue);

    cout << iRet;

    return 0;

} // End of main

