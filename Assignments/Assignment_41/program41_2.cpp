int Sum(int iNo)
{
    if (iNo == 0)
    {
        return 0;
    }

    return ((iNo % 10) + Sum(iNo / 10));

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout << "Enter the Number : \n";
    cin >> iValue;

    iRet = Sum(iValue);

    cout << iRet;

    return 0;

} // End of main

