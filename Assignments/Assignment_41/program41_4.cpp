#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Fact
//  Description   : Write a recursive program which Accept number from user and
//                  return its factorial.
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 4/11/2025
//
////////////////////////////////////////////////////////////////////////////////

int Fact(int iNo)
{
    if (iNo == 0)
    {
        return 1;
    }

    return (iNo * Fact(iNo - 1));

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout << "Enter the Character : \n";
    cin >> iValue;

    iRet = Fact(iValue);

    cout << iRet;

    return 0;

} // End of main

