#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Swap
//  Description   : Write Generic program to swap two values.
//  Input         : Generic
//  Output        : Generic
//  Author        : Rahul Balasaheb Ugale
//  Date          : 09/01/2026
//
////////////////////////////////////////////////////////////////////////////////

template <class T>
T Swap(T &No1, T &No2)
{
    T temp = No1;
    No1 = No2;
    No2 = temp;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int a = 10, b = 20;
    float x = 10.5f, y = 25.5f;

    Swap(a, b);
    cout << "After swap (int): " << a << " " << b << "\n";

    Swap(x, y);
    cout << "After swap (float): " << x << " " << y << "\n";

    return 0;
}

