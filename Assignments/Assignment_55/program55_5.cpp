template <class T>
T Max(T A, T B, T C, T D)
{
    T max = A;

    if (B > max)
    {
        max = B;
    }

    if (C > max)
    {
        max = C;
    }

    if (D > max)
    {
        max = D;
    }

    return max;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = Max(10, 20, 5, 15);
    cout << "Maximum (int) : " << iRet << "\n";

    float fRet = Max(10.5f, 25.5f, 7.2f, 18.4f);
    cout << "Maximum (float) : " << fRet << "\n";

    return 0;
}

