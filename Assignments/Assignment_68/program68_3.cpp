void Display()
{
    static int iNo = 5;

    if (iNo >= 1)
    {
        cout << iNo << "\t";
        iNo--;
        Display();
    }
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}

