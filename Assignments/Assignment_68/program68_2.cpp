void Display()
{
    static int iNo = 1;

    if (iNo <= 5)
    {
        cout << iNo << "\t";
        iNo++;
        Display();
    }
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}

