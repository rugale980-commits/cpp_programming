#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Write a recursive program which display below patten.
//  Input         : Integer
//  Output        : Character
//  Author        : Rahul Balasaheb Ugale
//  Date          : 06/02/2026
//
////////////////////////////////////////////////////////////////////////////////

void Display()
{
    static char Ch = 'A';

    if (Ch <= 'F')
    {
        cout << Ch << "\t";
        Ch++;
        Display();
    }
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}

