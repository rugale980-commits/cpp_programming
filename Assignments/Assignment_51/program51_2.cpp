template <class T>
int Frequence(T *Arr, int Size, T No)
{
    int iCnt = 0;
    int iCount = 0;

    for (iCnt = 0; iCnt < Size; iCnt++)
    {
        if (Arr[iCnt] == No)
        {
            iCount++;
        }
    }

    return iCount;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    int arr[] = {10, 20, 30, 10, 30, 40, 10, 40, 10};

    iRet = Frequence(arr, 9, 10);

    printf("%d", iRet);

    return 0;
}

