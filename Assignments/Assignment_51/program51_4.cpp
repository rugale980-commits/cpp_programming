using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SearchLast
//  Description   : Write Generic program to accept N value and Search Last
//                  occurrence of any specific value.
//  Input         : Generic
//  Output        : Generic
//  Author        : Rahul Balasaheb Ugale
//  Date          : 08/01/2026
//
