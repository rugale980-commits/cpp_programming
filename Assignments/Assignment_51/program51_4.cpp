template <class T>
int SearchLast(T *Arr, int Size, T No)
{
    int iCnt = 0;

    for (iCnt = Size - 1; iCnt >= 0; iCnt--)
    {
        if (Arr[iCnt] == No)
        {
            return iCnt;
        }
    }

    return -1;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    int arr[] = {10, 20, 30, 10, 30, 40, 10, 40, 10};

    iRet = SearchLast(arr, 9, 40);

    printf("%d", iRet);

    return 0;
}

