#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Write Generic program which accept one value and one number
//                  from user.print that value that number of times on screen.
//  Input         : Generic
//  Output        : Generic
//  Author        : Rahul Balasaheb Ugale
//  Date          : 08/01/2026
//
////////////////////////////////////////////////////////////////////////////////

template <class T>
void Display(T value, int Size)
{
    int iCnt = 0;

    for (iCnt = 0; iCnt < Size; iCnt++)
    {
        cout << value << " ";
    }
    cout << "\n";
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display('M', 7);
    Display(11, 3);
    Display(3.7, 6);

    return 0;
}

