void Display(int iNo)
{
    if (iNo == 0)
    {
        return;
    }

    cout << "*\t";

    Display(iNo - 1);

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display(5);

    return 0;

} // End of main

