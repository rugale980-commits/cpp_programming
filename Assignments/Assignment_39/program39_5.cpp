void Display(char ch)
{
    if (ch < 'a')
    {
        return;
    }

    Display(ch - 1);

    cout << ch << "\t";

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display('f');

    return 0;

} // End of main

