void Display(int iNo)
{
    if (iNo == 0)
    {
        return;
    }

    Display(iNo - 1);
    cout << iNo << "\t";
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter the number : " << "\n";
    cin >> iValue;

    Display(iValue);

    return 0;
}

