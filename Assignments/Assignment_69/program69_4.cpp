#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Write a recursive program which display below patten.
//  Input         : Integer
//  Output        : Character
//  Author        : Rahul Balasaheb Ugale
//  Date          : 06/02/2026
//
////////////////////////////////////////////////////////////////////////////////

void Display(int iNo, char Ch)
{
    if (iNo == 0)
    {
        return;
    }

    cout << Ch << "\t";
    Display(iNo - 1, Ch + 1);
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter the number : " << "\n";
    cin >> iValue;

    Display(iValue, 'A');

    return 0;
}

