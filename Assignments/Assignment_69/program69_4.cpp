#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Write a recursive program which display below patten.
//  Input         : Integer
//  Output        : Character
//  Author        : Rahul Balasaheb Ugale
//  Date          : 06/02/2026
//
////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static char Ch = 'A';

    if (iNo >= 1)
    {
        cout << Ch << "\t";
        Ch++;
        iNo--;
        Display(iNo);
    }
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter the number : " << "\n";
    cin >> iValue;

    Display(iValue);

    return 0;
}

