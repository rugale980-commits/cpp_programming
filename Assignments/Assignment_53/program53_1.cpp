template <class T>
T CopyArray(T *Src, T *Dest, int iSize)
{
    int iCnt = 0;

    for (iCnt = 0; iCnt < iSize; iCnt++)
    {
        Dest[iCnt] = Src[iCnt];
    }
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 30, 40, 50};
    int brr[5];
    int iCnt = 0;

    CopyArray(arr, brr, 5);

    cout << "Copied array:\n";
    for (iCnt = 0; iCnt < 5; iCnt++)
    {
        cout << brr[iCnt] << " ";
    }

    return 0;
}

