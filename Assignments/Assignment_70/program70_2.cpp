int Sum(int iNo)
{
    static int iSum = 0;
    static int iDigit = 0;

    if (iNo != 0)
    {
        iDigit = iNo % 10;
        iSum = iSum + iDigit;
        Sum(iNo / 10);
    }
    return iSum;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    cout << "Enter the number : " << "\n";
    cin >> iValue;

    iRet = Sum(iValue);

    cout << iRet;

    return 0;
}

