#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Fact
//  Description   : Write a recursive program which accept number from user and
//                  return its factorial.
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 09/02/2026
//
////////////////////////////////////////////////////////////////////////////////

int Fact(int iNo)
{
    static int iFact = 1;

    if (iNo != 0)
    {
        iFact = iFact * iNo;
        iNo--;
        Fact(iNo);
    }
    return iFact;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    cout << "Enter the number : " << "\n";
    cin >> iValue;

    iRet = Fact(iValue);

    cout << iRet;

    return 0;
}

