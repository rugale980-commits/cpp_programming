#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Mul
//  Description   : Write a recursive program which accept number from user and
//                  return its product of digit.
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 09/02/2026
//
////////////////////////////////////////////////////////////////////////////////

int Mul(int iNo)
{
    static int iMul = 1;
    static int iDigit = 0;

    if (iNo != 0)
    {
        iDigit = iNo % 10;
        iMul = iMul * iDigit;
        Mul(iNo / 10);
    }
    return iMul;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    cout << "Enter the number : " << "\n";
    cin >> iValue;

    iRet = Mul(iValue);

    cout << iRet;

    return 0;
}

