template <class T>
T SumEven(T *Arr, int iSize)
{
    T Sum = 0;
    int i = 0;

    for (i = 0; i < iSize; i++)
    {
        if (Arr[i] % 2 == 0)
        {
            Sum = Sum + Arr[i];
        }
    }

    return Sum;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 5, 40, 50};

    int iRet = SumEven(arr, 5);
    cout << iRet << "\n";

    return 0;
}

