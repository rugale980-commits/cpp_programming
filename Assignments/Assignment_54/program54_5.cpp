////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Min
//  Description   : Write Generic program to find smallest element from an array.
//  Input         : Generic
//  Output        : Generic
//  Author        : Rahul Balasaheb Ugale
//  Date          : 09/01/2026
