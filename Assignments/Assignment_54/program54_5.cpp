template <class T>
T Min(T *Arr, int iSize)
{
    T imin = Arr[0];
    int i = 0;

    for (i = 1; i < iSize; i++)
    {
        if (Arr[i] < imin)
        {
            imin = Arr[i];
        }
    }

    return imin;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 5, 40, 50};

    int iRet = Min(arr, 5);
    cout << iRet << "\n";

    return 0;
}

