template <class T>
void Reverse(T *Arr, int iSize)
{
    int start = 0;
    int end = iSize - 1;

    for (start < end; start++, end--;)
    {
        T temp = Arr[start];
        Arr[start] = Arr[end];
        Arr[end] = temp;
    }
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 5, 40, 50};

    Reverse(arr, 5);

    for (int i = 0; i < 5; i++)
    {
        cout << arr[i] << " ";
    }

    return 0;
}

