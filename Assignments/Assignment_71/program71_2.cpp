////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Max
//  Description   : Write a recursive program which accept number from user and
//                  return largest digit.
//  Input         : Integer
//  Output        : Count Maxmimum NUmber
//  Author        : Rahul Balasaheb Ugale
