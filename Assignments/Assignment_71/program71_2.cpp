int Max(int iNo)
{
    static int iMax = 0;
    int iDigit = 0;

    if (iNo != 0)
    {
        iDigit = iNo % 10;

        if (iDigit > iMax)
        {
            iMax = iDigit;
        }

        Max(iNo / 10);
    }

    return iMax;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout << "Enter the Number : " << "\n";
    cin >> iValue;

    iRet = Max(iValue);

    cout << "Largest number is : " << iRet;

    return 0;
}

