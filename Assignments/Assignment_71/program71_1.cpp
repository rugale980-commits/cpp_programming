////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : WhiteSpace
//  Description   : Write a recursive program which accept string from user and
//                  count white spaces.
//  Input         : String
//  Output        : Count White space
//  Author        : Rahul Balasaheb Ugale
//  Date          : 09/02/2026
//
////////////////////////////////////////////////////////////////////////////////

int WhiteSpace(char *str)
{
    int iCount = 0;

    if (*str != '\0')
    {
        if (*str == ' ')
        {
            iCount++;
        }

        str++;

        iCount = iCount + WhiteSpace(str);
    }

    return iCount;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    string Ch;
    int iRet = 0;

    cout << "Enter the String : " << "\n";

    getline(cin, Ch);

    iRet = WhiteSpace(&Ch[0]);

    cout << iRet;

    return 0;
}

