template <class T>
T Min(T *Arr, int iSize)
{
    T imin = Arr[0];

    for (int i = 1; i < iSize; i++)
    {
        if (Arr[i] < imin)
        {
            imin = Arr[i];
        }
    }

    return imin;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 30, 40, 50};
    float brr[] = {10.0f, 3.7f, 9.8f, 8.7f};

    int iRet = Min(arr, 5);
    cout << iRet << "\n";

    float fRet = Min(brr, 4);
    cout << fRet << "\n";

    return 0;
}

