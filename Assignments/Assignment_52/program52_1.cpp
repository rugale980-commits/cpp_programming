template <class T>
T Multiply(T No1, T No2)
{
    T ans;
    ans = No1 * No2;
    return ans;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = Multiply(10, 20);
    cout << iRet << "\n";

    float fRet = Multiply(10.0f, 20.0f);
    cout << fRet << "\n";

    return 0;
}

