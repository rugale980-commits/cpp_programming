template <class T>
T AddN(T *Arr, int Size)
{
    T Sum = 0;
    int iCnt = 0;

    for (iCnt = 0; iCnt < Size; iCnt++)
    {
        Sum = Sum + Arr[iCnt];
    }

    return Sum;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 30, 40, 50};
    float brr[] = {10.0f, 20.0f, 30.0f, 40.0f};

    int iSum = AddN(arr, 5);
    cout << iSum << "\n";

    float fSum = AddN(brr, 4);
    cout << fSum << "\n";

    return 0;
}

