template <class T>
T Max(T No1, T No2, T No3)
{
    T imax = No1;

    if (No2 > imax)
    {
        imax = No2;
    }

    if (No3 > imax)
    {
        imax = No3;
    }

    return imax;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = Max(10, 20, 30);
    cout << iRet << "\n";

    float fRet = Max(10.0f, 20.0f, 30.0f);
    cout << fRet << "\n";

    return 0;
}

